import { addShip } from "./addShip.js";
import { Ship } from "../01-ship/ship.js";

test("Should place ship in board", () => {
  let ship = new Ship("Patrol", 2);
  let board = [
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [[], [], [], [], [], [], [], [], [], []],
    [ship, [], [], [], [], [], [], [], [], []],
    [ship, [], [], [], [], [], [], [], [], []],
  ];
  addShip(ship, [0, 0], [0, 1], board);

  expect(board[0][0]).toBe(ship);
  expect(board[0][1]).toBe(ship);
});
